STA 1200H 	
MVI A,08H 	
STA 1201H 	


SUB B	
MOV D,A
INR D
LDA 1200H

MVI L,00H
MVI H,00H
LDA 1201H

MOV E,A

BUCLE:

MOV A,L
ADD E
MOV L,A
JNC NOACARREO
	INR H
NOACARREO:
DCR E
DCR D
MOV A,D
